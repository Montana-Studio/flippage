        $(document).ready(function(){ 
            // URIs - Format #/page/1 
            Hash.on('^page\/([0-9]*)$', {
                yep: function(path, parts) {
                    var page = parts[1];

                    if (page!==undefined) {
                        if ($('#flipbook').turn('is')) {
                            $('#flipbook').turn('page', page);
                        }
                    }

                },
                nop: function(path) {

                    if ($('#flipbook').turn('is')) {
                        $('#flipbook').turn('page', 1);
                    }
                }
            });
        });
